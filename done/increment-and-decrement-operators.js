// Increment and Decrement Operators
var number = 0;
// // Postfix
console.log("postfix")
console.log(number++)   // 0
console.log(number)     // 1
console.log(number--)   // 1
console.log(number)     // 0
// 先印 後加減


// Prefix
// 先加減 後印
console.log("prefix")
var numberTwo = 0;
console.log(++numberTwo); // 1
console.log(--numberTwo); // 0

// console.log("for loop")
for (var i = 0; i <= 10; i++) {
    console.log(i);
}