// While Loops
var names = [
    "Alex",
    "Jamila",
    "Joe",
    "Aisha",
    "Bob"
];

var index = 0;


while(index < names.length) { // 0 < 5 ->   0 1 2 3 4
    console.log(names[index]);
    index = index + 1;
}


